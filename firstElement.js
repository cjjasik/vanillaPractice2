// return the first element of an array of Strings
// return the value of the element of the array provided given an index

/**
 * 
 * @param {String} words // passed into the function
 * @returns {String} // element in array.
 */


function firstElement (words, num) {

    return words[num];
}


console.log(firstElement(["apple", "banana", "carrot"], 1));